 import React, { useState, useEffect } from 'react';
import axios from 'axios';

const ContentIncomes = () => {
  const [isFormOpen, setIsFormOpen] = useState(false);
  const [formData, setFormData] = useState({
    amount: '',
    date: '',
    type: '',
    description: '',
  });
  const [incomes, setIncomes] = useState([]);
  const [editingIncomeId, setEditingIncomeId] = useState(null);

   const [filters, setFilters] = useState({
    month: '',
    year: '',
    type: '',
    minAmount: '',
    maxAmount: '', 
  });
  const [sortConfig, setSortConfig] = useState({
    key: 'createdAt', 
    direction: 'ascending', 
  })

  const incomesColumns = [
    { name: 'Id', key: 'id' },
    { name: 'Amount', key: 'amount' },
    { name: 'Date', key: 'date' },
    { name: 'Type', key: 'type' },
    { name: 'Description', optional: true, key: 'description' },
    { name: 'Creation Date', autoGenerated: true, key: 'createdAt' },
    { name: '', isAction: true },
  ];

  useEffect(() => {
    fetchIncomes();
  }, [filters, sortConfig]);

  const fetchIncomes = async () => {
    const token = localStorage.getItem('token');
    if (!token) return;

    try {
      let url = 'http://localhost:5000/api/incomes';
      const params = {};

      if (filters.month) params.month = filters.month;
      if (filters.year) params.year = filters.year;
      if (filters.type) params.type = filters.type;
      if (filters.minAmount) params.minAmount = filters.minAmount;
      if (filters.maxAmount) params.maxAmount = filters.maxAmount;

      const response = await axios.get(url, {
        headers: { Authorization: `Bearer ${token}` },
        params,
      });
      let sortedIncomes = [...response.data];

      sortedIncomes.sort((a, b) => {
        let aValue = a[sortConfig.key];
        let bValue = b[sortConfig.key];

        if (sortConfig.key === 'createdAt' || sortConfig.key === 'date') {
          aValue = aValue ? new Date(aValue) : new Date(0);
          bValue = bValue ? new Date(bValue) : new Date(0);
          if (isNaN(aValue)) aValue = new Date(0);
          if (isNaN(bValue)) bValue = new Date(0);
        } else {
          aValue = aValue || '';
          bValue = bValue || '';
        }

        if (aValue < bValue) {
          return sortConfig.direction === 'ascending' ? -1 : 1;
        }
        if (aValue > bValue) {
          return sortConfig.direction === 'ascending' ? 1 : -1;
        }
        return 0;
      });

      setIncomes(sortedIncomes);
    } catch (error) {
      console.error('Erreur lors du fetch des revenus:', error.response?.data || error);
    }
  };

  const handleChange = (e) => {
    setFormData({ ...formData, [e.target.name]: e.target.value });
  };

  const handleFilterChange = (e) => {
    const { name, value } = e.target;
    setFilters((prev) => ({ ...prev, [name]: value }));
  };

  const requestSort = (key) => {
    setSortConfig((prev) => ({
      key,
      direction:
        prev.key === key && prev.direction === 'ascending' ? 'descending' : 'ascending',
    }));
  };

  const handleEdit = (income) => {
    setFormData({
      amount: income.amount,
      date: income.date.split('T')[0],
      type: income.type || '',
      description: income.description || '',
    });
    setEditingIncomeId(income.id);
    setIsFormOpen(true);
  };

  const handleDelete = async (id) => {
    const token = localStorage.getItem('token');
    if (!token) return;
    if (!window.confirm('Voulez-vous vraiment supprimer ce revenu ?')) return;

    try {
      await axios.delete(`http://localhost:5000/api/incomes/${id}/delete`, {
        headers: { Authorization: `Bearer ${token}` },
      });
      setIncomes((prev) => prev.filter((i) => i.id !== id));
    } catch (error) {
      console.error('Erreur suppression:', error.response?.data || error);
      alert(`Erreur: ${error.response?.data?.message || error.message}`);
    }
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    const token = localStorage.getItem('token');
    if (!token) {
      alert('Vous devez être connecté pour ajouter ou modifier un revenu');
      return;
    }

    try {
      if (editingIncomeId) {
        await axios.put(
          `http://localhost:5000/api/incomes/${editingIncomeId}/edit`,
          {
            amount: parseFloat(formData.amount),
            date: formData.date,
            type: formData.type,
            description: formData.description,
          },
          { headers: { Authorization: `Bearer ${token}` } }
        );

        setIncomes((prev) => {
          const index = prev.findIndex((i) => i.id === editingIncomeId);
          if (index === -1) return prev;
          const updated = [...prev];
          updated[index] = {
            ...updated[index],
            amount: parseFloat(formData.amount),
            date: formData.date,
            type: formData.type,
            description: formData.description,
          };
          return updated.sort((a, b) => {
            let aValue = a[sortConfig.key];
            let bValue = b[sortConfig.key];
            if (sortConfig.key === 'createdAt' || sortConfig.key === 'date') {
              aValue = aValue ? new Date(aValue) : new Date(0);
              bValue = bValue ? new Date(bValue) : new Date(0);
              if (isNaN(aValue)) aValue = new Date(0);
              if (isNaN(bValue)) bValue = new Date(0);
            } else {
              aValue = aValue || '';
              bValue = bValue || '';
            }
            return sortConfig.direction === 'ascending'
              ? aValue < bValue ? -1 : aValue > bValue ? 1 : 0
              : aValue > bValue ? -1 : aValue < bValue ? 1 : 0;
          });
        });

        setEditingIncomeId(null);
      } else {
        const response = await axios.post(
          'http://localhost:5000/api/incomes/new',
          {
            amount: parseFloat(formData.amount),
            date: formData.date,
            type: formData.type,
            description: formData.description,
          },
          { headers: { Authorization: `Bearer ${token}` } }
        );
        setIncomes((prev) => {
          const updated = [...prev, response.data];
          return updated.sort((a, b) => {
            let aValue = a[sortConfig.key];
            let bValue = b[sortConfig.key];
            if (sortConfig.key === 'createdAt' || sortConfig.key === 'date') {
              aValue = aValue ? new Date(aValue) : new Date(0);
              bValue = bValue ? new Date(bValue) : new Date(0);
              if (isNaN(aValue)) aValue = new Date(0);
              if (isNaN(bValue)) bValue = new Date(0);
            } else {
              aValue = aValue || '';
              bValue = bValue || '';
            }
            return sortConfig.direction === 'ascending'
              ? aValue < bValue ? -1 : aValue > bValue ? 1 : 0
              : aValue > bValue ? -1 : aValue < bValue ? 1 : 0;
          });
        });
      }

      setIsFormOpen(false);
      setFormData({ amount: '', date: '', type: '', description: '' });
    } catch (error) {
      console.error('Erreur:', error.response?.data || error);
      alert(`Erreur: ${error.response?.data?.message || error.message}`);
    }
  };

  // Extraire les types uniques à partir des incomes
  const uniqueTypes = [...new Set(incomes.map(income => income.type).filter(type => type))];

  const totalAmount = incomes.reduce((sum, income) => sum + parseFloat(income.amount || 0), 0);
  const incomeCount = incomes.length;
  const latestDate = incomes.length > 0 ? incomes[incomes.length - 1].createdAt?.split('T')[0] : 'N/A';

  return (
    <div className="w-full min-h-screen bg-gradient-to-b from-gray-50 to-gray-100 flex flex-col items-center p-4 sm:p-6">
      <h1 className="text-3xl md:text-4xl font-extrabold mb-8 text-gray-900 dark:text-white tracking-wide drop-shadow-md">
        Income Tracking Dashboard
      </h1>

      {/* Filters Section */}
      <div className="w-full bg-white rounded-2xl p-6 mb-6 shadow-lg transform transition-all duration-300 hover:shadow-xl">
        <div className="grid grid-cols-1 sm:grid-cols-5 gap-6">
          {/* Month Filter */}
          <div className="relative">
            <label className="block text-sm font-medium text-gray-700 mb-2">Month</label>
            <select
              name="month"
              value={filters.month}
              onChange={handleFilterChange}
              className="w-full px-4 py-2 border border-gray-300 rounded-lg bg-white text-gray-900 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:border-transparent transition-all duration-200 hover:bg-gray-50"
            >
              <option value="">All</option>
              {Array.from({ length: 12 }, (_, i) => i + 1).map((m) => (
                <option key={m} value={m}>{new Date(0, m - 1).toLocaleString('default', { month: 'long' })}</option>
              ))}
            </select>
          </div>

          {/* Year Filter */}
          <div className="relative">
            <label className="block text-sm font-medium text-gray-700 mb-2">Year</label>
            <select
              name="year"
              value={filters.year}
              onChange={handleFilterChange}
              className="w-full px-4 py-2 border border-gray-300 rounded-lg bg-white text-gray-900 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:border-transparent transition-all duration-200 hover:bg-gray-50"
            >
              <option value="">All</option>
              {Array.from({ length: 5 }, (_, i) => new Date().getFullYear() - i).map((y) => (
                <option key={y} value={y}>{y}</option>
              ))}
            </select>
          </div>

          {/* Type Filter */}
          <div className="relative">
            <label className="block text-sm font-medium text-gray-700 mb-2">Type</label>
            <select
              name="type"
              value={filters.type}
              onChange={handleFilterChange}
              className="w-full px-4 py-2 border border-gray-300 rounded-lg bg-white text-gray-900 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:border-transparent transition-all duration-200 hover:bg-gray-50"
            >
              <option value="">All</option>
              {uniqueTypes.map((type) => (
                <option key={type} value={type}>{type}</option>
              ))}
            </select>
          </div>

          {/* Minimum Amount Filter */}
          <div className="relative">
            <label className="block text-sm font-medium text-gray-700 mb-2">Min Amount</label>
            <input
              type="number"
              name="minAmount"
              value={filters.minAmount}
              onChange={handleFilterChange}
              placeholder="Min Ar"
              className="w-full px-4 py-2 border border-gray-300 rounded-lg bg-white text-gray-900 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:border-transparent transition-all duration-200 hover:bg-gray-50"
              min="0"
              step="0.01"
            />
          </div>

          {/* Maximum Amount Filter */}
          <div className="relative">
            <label className="block text-sm font-medium text-gray-700 mb-2">Max Amount</label>
            <input
              type="number"
              name="maxAmount"
              value={filters.maxAmount}
              onChange={handleFilterChange}
              placeholder="Max Ar"
              className="w-full px-4 py-2 border border-gray-300 rounded-lg bg-white text-gray-900 focus:outline-none focus:ring-2 focus:ring-indigo-500 focus:border-transparent transition-all duration-200 hover:bg-gray-50"
              min="0"
              step="0.01"
            />
          </div>
        </div>
      </div>

      {/* Cards Section */}
      <div className="w-full grid grid-cols-1 sm:grid-cols-3 gap-6 mb-8">
        {/* Card 1: Total Income */}
        <div className="bg-gradient-to-br from-blue-600 to-indigo-700 text-white rounded-xl p-6 shadow-xl transform transition-all duration-300 hover:-translate-y-2 hover:shadow-2xl">
          <div className="text-center">
            <h3 className="text-sm font-semibold uppercase tracking-wide">Total Income</h3>
            <p className="text-3xl font-extrabold mt-2">{totalAmount.toFixed(2)} Ar</p>
          </div>
        </div>

        {/* Card 2: Number of Incomes */}
        <div className="bg-gradient-to-br from-green-600 to-teal-700 text-white rounded-xl p-6 shadow-xl transform transition-all duration-300 hover:-translate-y-2 hover:shadow-2xl">
          <div className="text-center">
            <h3 className="text-sm font-semibold uppercase tracking-wide">Income Count</h3>
            <p className="text-3xl font-extrabold mt-2">{incomeCount}</p>
          </div>
        </div>

        {/* Card 3: Latest Income Date */}
        <div className="bg-gradient-to-br from-purple-600 to-pink-700 text-white rounded-xl p-6 shadow-xl transform transition-all duration-300 hover:-translate-y-2 hover:shadow-2xl">
          <div className="text-center">
            <h3 className="text-sm font-semibold uppercase tracking-wide">Latest Income</h3>
            <p className="text-3xl font-extrabold mt-2">{latestDate}</p>
          </div>
        </div>
      </div>

      {/* Table Section */}
      <div className="w-full bg-white rounded-2xl shadow-xl">
        <table className="w-full table-auto border-collapse">
          <thead className="bg-gradient-to-r from-blue-600 to-indigo-600 text-white">
            <tr>
              {incomesColumns.map((col) => (
                <th
                  key={col.key || col.name}
                  className="p-4 text-center font-semibold text-sm md:text-base cursor-pointer"
                  onClick={() => col.key && requestSort(col.key)}
                >
                  {col.name}
                  {col.optional && <span className="text-xs ml-1 font-normal">(optional)</span>}
                  {col.autoGenerated && <span className="text-xs ml-1 font-normal">(auto)</span>}
                  {sortConfig.key === col.key && (
                    <span>{sortConfig.direction === 'ascending' ? ' ↑' : ' ↓'}</span>
                  )}
                </th>
              ))}
            </tr>
          </thead>
          <tbody>
            {incomes.map((income, index) => (
              <tr
                key={income.id}
                className="transition-all duration-300 hover:bg-indigo-50 hover:scale-[1.02] cursor-pointer"
              >
                <td className="p-4 border-b border-gray-200 text-center font-medium">{index + 1}</td>
                <td className="p-4 border-b border-gray-200 text-center font-medium">{income.amount} Ar</td>
                <td className="p-4 border-b border-gray-200 text-center">{income.date?.split('T')[0]}</td>
                <td className="p-4 border-b border-gray-200 text-center">{income.type}</td>
                <td className="p-4 border-b border-gray-200 text-center">{income.description}</td>
                <td className="p-4 border-b border-gray-200 text-center">{income.createdAt?.split('T')[0]}</td>
                <td className="p-4 border-b border-gray-200 text-center flex justify-center gap-3">
                  <button
                    onClick={() => handleEdit(income)}
                    className="bg-yellow-400 hover:bg-yellow-500 text-white font-semibold px-4 py-2 rounded-lg shadow-md transition-all"
                  >
                    Modify
                  </button>
                  <button
                    onClick={() => handleDelete(income.id)}
                    className="bg-red-500 hover:bg-red-600 text-white font-semibold px-4 py-2 rounded-lg shadow-md transition-all"
                  >
                    Delete
                  </button>
                </td>
              </tr>
            ))}
            <tr className="bg-gray-100 font-bold">
              <td className="p-4 border-b border-gray-200 text-center">Total</td>
              <td className="p-4 border-b border-gray-200 text-center font-medium">{totalAmount.toFixed(2)} Ar</td>
              <td className="p-4 border-b border-gray-200 text-center"></td>
              <td className="p-4 border-b border-gray-200 text-center"></td>
              <td className="p-4 border-b border-gray-200 text-center"></td>
              <td className="p-4 border-b border-gray-200 text-center"></td>
              <td className="p-4 border-b border-gray-200 text-center"></td>
            </tr>
            <tr>
              <td colSpan={7} className="p-6 text-center">
                <button
                  onClick={() => { setIsFormOpen(!isFormOpen); setEditingIncomeId(null); }}
                  className="bg-blue-600 hover:scale-105 transition-all text-white font-bold py-3 px-8 rounded-full shadow-2xl flex items-center justify-center gap-3 mx-auto"
                >
                  <span className="text-2xl font-extrabold">+</span>
                  <span>{editingIncomeId ? 'Edit Income' : 'Add Income'}</span>
                </button>

                {isFormOpen && (
                  <div className="fixed inset-0 bg-black bg-opacity-30 flex items-center justify-center z-50">
                    <div className="bg-white rounded-2xl p-8 w-full max-w-lg mx-4 shadow-2xl">
                      <h2 className="text-2xl font-extrabold mb-6 text-gray-800">
                        {editingIncomeId ? 'Edit Income' : 'Add Income'}
                      </h2>
                      <form onSubmit={handleSubmit} className="flex flex-col gap-4">
                        <input
                          type="number"
                          name="amount"
                          placeholder="Amount"
                          value={formData.amount}
                          onChange={handleChange}
                          className="border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-indigo-500 focus:outline-none shadow-sm"
                          required
                          step="0.01"
                          min="0"
                        />
                        <input
                          type="date"
                          name="date"
                          value={formData.date}
                          onChange={handleChange}
                          className="border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-indigo-500 focus:outline-none shadow-sm"
                          required
                        />
                        <input
                          type="text"
                          name="type"
                          placeholder="Type"
                          value={formData.type}
                          onChange={handleChange}
                          className="border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-indigo-500 focus:outline-none shadow-sm"
                          required
                        />
                        <input
                          type="text"
                          name="description"
                          placeholder="Description (optional)"
                          value={formData.description}
                          onChange={handleChange}
                          className="border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-indigo-500 focus:outline-none shadow-sm"
                        />
                        <div className="flex justify-end gap-3 mt-4">
                          <button
                            type="button"
                            onClick={() => { setIsFormOpen(false); setEditingIncomeId(null); }}
                            className="bg-gray-300 hover:bg-gray-400 text-gray-800 font-semibold py-2 px-6 rounded-lg shadow-sm transition-all"
                          >
                            Cancel
                          </button>
                          <button
                            type="submit"
                            className="bg-gradient-to-r from-indigo-500 via-purple-500 to-pink-500 text-white font-bold py-2 px-6 rounded-lg shadow-lg hover:scale-105 transition-all"
                          >
                            {editingIncomeId ? 'Update' : 'Save'}
                          </button>
                        </div>
                      </form>
                    </div>
                  </div>
                )}
              </td>
            </tr>
          </tbody>
        </table>
      </div>
    </div>
  );
};

export default ContentIncomes;