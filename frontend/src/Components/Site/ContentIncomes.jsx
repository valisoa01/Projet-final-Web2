import React, { useState, useEffect } from 'react';
import axios from 'axios';

const ContentIncomes = () => {
  const [isFormOpen, setIsFormOpen] = useState(false);
  const [formData, setFormData] = useState({
    amount: '',
    date: '',
    type: '',        // changé ici
    description: '',
  });
  const [incomes, setIncomes] = useState([]);

  const incomesColumns = [
    { name: 'Id' },
    { name: 'Amount' },
    { name: 'Date' },
    { name: 'Type' },   // changé ici
    { name: 'Description', optional: true },
    { name: 'Creation Date', autoGenerated: true },
  ];

  // Fetch des revenus existants depuis le backend
  useEffect(() => {
    const fetchIncomes = async () => {
      try {
        const response = await axios.get('http://localhost:5000/api/incomes');
        setIncomes(response.data);
      } catch (error) {
        console.error('Erreur lors du fetch des revenus:', error);
      }
    };

    fetchIncomes();
  }, []);

  const handleChange = (e) => {
    setFormData({ ...formData, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    try {
      const response = await axios.post('http://localhost:5000/api/incomes', {
        ...formData,
        UserId: 1, // remplacez par l'id de l'utilisateur connecté si besoin
      });

      setIncomes([...incomes, response.data]);

      setIsFormOpen(false);
      setFormData({ amount: '', date: '', type: '', description: '' }); // changé ici
    } catch (error) {
      console.error('Erreur lors de la création du revenu:', error.response?.data || error);
      alert('Erreur lors de la création du revenu');
    }
  };

  return (
    <div className="w-full mt-6 flex flex-col items-center p-4">
      <h1 className="text-2xl md:text-3xl font-bold mb-6 text-gray-800">
        Income Tracking Table
      </h1>

      <div className="w-full md:w-4/5 overflow-auto shadow-lg rounded-lg border border-gray-300">
        <table className="w-full table-auto border-collapse">
          <thead className="bg-gray-200">
            <tr>
              {incomesColumns.map((col, index) => (
                <th
                  key={index}
                  className="p-3 border-b border-gray-300 text-center font-semibold text-sm"
                >
                  {col.name}
                  {col.optional && <span className="text-xs text-gray-500 ml-1">(optional)</span>}
                  {col.autoGenerated && <span className="text-xs text-gray-500 ml-1">(auto)</span>}
                </th>
              ))}
            </tr>
          </thead>
          <tbody className="bg-white">
            {incomes.map((income) => (
              <tr key={income.id} className="hover:bg-gray-50">
                <td className="p-3 border-b border-gray-300 text-center">{income.id}</td>
                <td className="p-3 border-b border-gray-300 text-center">${income.amount}</td>
                <td className="p-3 border-b border-gray-300 text-center">{income.date}</td>
                <td className="p-3 border-b border-gray-300 text-center">{income.type}</td> {/* changé ici */}
                <td className="p-3 border-b border-gray-300 text-center">{income.description}</td>
                <td className="p-3 border-b border-gray-300 text-center">{income.createdAt}</td>
              </tr>
            ))}

            <tr>
              <td colSpan={6} className="p-4 text-center relative">
                <button
                  onClick={() => setIsFormOpen(!isFormOpen)}
                  className="bg-blue-500 hover:bg-blue-600 text-white font-semibold py-2 px-6 rounded-full shadow-md flex items-center justify-center gap-2 transition-all mx-auto"
                >
                  <span className="text-xl font-bold">+</span>
                  <span>Add Income</span>
                </button>

                {isFormOpen && (
                  <div className="fixed inset-0 bg-opacity-50 flex items-center justify-center z-50">
                    <div className="bg-white rounded-lg p-6 w-full max-w-md mx-4">
                      <h2 className="text-lg font-bold mb-4">Add Income</h2>
                      <form onSubmit={handleSubmit} className="flex flex-col gap-3">
                        <input
                          type="number"
                          name="amount"
                          placeholder="Amount"
                          value={formData.amount}
                          onChange={handleChange}
                          className="border p-2 rounded"
                          required
                          step="0.01"
                          min="0"
                        />
                        <input
                          type="date"
                          name="date"
                          value={formData.date}
                          onChange={handleChange}
                          className="border p-2 rounded"
                          required
                        />
                        <input
                          type="text"
                          name="type" // changé ici
                          placeholder="Type"
                          value={formData.type}
                          onChange={handleChange}
                          className="border p-2 rounded"
                          required
                        />
                        <input
                          type="text"
                          name="description"
                          placeholder="Description (optional)"
                          value={formData.description}
                          onChange={handleChange}
                          className="border p-2 rounded"
                        />
                        <div className="flex justify-end gap-2 mt-3">
                          <button
                            type="button"
                            onClick={() => setIsFormOpen(false)}
                            className="bg-gray-300 hover:bg-gray-400 text-black px-4 py-2 rounded"
                          >
                            Cancel
                          </button>
                          <button
                            type="submit"
                            className="bg-blue-500 hover:bg-blue-600 text-white px-4 py-2 rounded"
                          >
                            Save
                          </button>
                        </div>
                      </form>
                    </div>
                  </div>
                )}
              </td>
            </tr>
          </tbody>
        </table>
      </div>
    </div>
  );
};

export default ContentIncomes;
